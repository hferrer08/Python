cadena = "Esto es un ejemplo de cadena de texto" #comillas dobles o comillas simples
cadena2 = 'Esto es un "ejemplo" de cadena de texto'
print(cadena)
print(cadena2)
cadena3 = "Esto es un \"ejemplo\" de cadena de texto"
print(cadena3)
cadena4 = "Esto es un \nejemplo de cadena de texto" #\n hace un enter, un espacio
print(cadena4)
cadena5 = "Esto es un \tejemplo de cadena de texto" #\t hace un espacio (tabulacion)
print(cadena5)